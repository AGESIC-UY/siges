package com.sofis.entities.enums;

/**
 *
 * @author usuario
 */
public class ColoresSemaforoEnum {
    private Integer id;
    private String nom;

    public ColoresSemaforoEnum(Integer id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    @Override
    public boolean equals(Object obj) {
       if (!(obj instanceof ColoresSemaforoEnum)) {
            return false;
        }
        ColoresSemaforoEnum other = (ColoresSemaforoEnum) obj;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }
    
    
    
    
}
